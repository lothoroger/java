package com.example.model;

public class User {
	public String fullName;
	public String email;
	public String password;
	public String street;
	public String city;
	public String state;
	public String country;
	public String pincode;
	public String image;
	public String contact;
	
	
	public User(String fullName, String email, String password, String street, String city, String state,
			String country, String pincode, String image, String contact) {
		super();
		this.fullName = fullName;
		this.email = email;
		this.password = password;
		this.street = street;
		this.city = city;
		this.state = state;
		this.country = country;
		this.pincode = pincode;
		this.image = image;
		this.contact = contact;
	}

	
	
	
	
	
}
